# Math operations with variables
a = 10
b = 5
sum_result= a + b
print(f"The sum of {a} and {b} is {sum_result}")

# comparing division and integer division
division_result = a / b
integer_division_result: int = a // b
print(f"The result of {a} divided by {b} is {division_result}")
print(f"The result of {a} integer divided by {b} is {integer_division_result}")

